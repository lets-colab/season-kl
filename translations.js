/* ─── Engine ─── */
(function(){
  var LANGS={en:'EN',zh:'中文',ko:'한국어',yue:'粤'};
  var current=localStorage.getItem('skl_lang')||'en';

  function t(key){var l=window.SKL_LANG[current];return(l&&l[key])||window.SKL_LANG.en[key]||'';}

  function applyAll(){
    document.querySelectorAll('[data-i18n]').forEach(function(el){
      var v=t(el.dataset.i18n);if(v)el.innerHTML=v;
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(function(el){
      var v=t(el.dataset.i18nPh);if(v)el.placeholder=v;
    });
    document.querySelectorAll('.lang-btn').forEach(function(b){
      b.classList.toggle('active',b.dataset.lang===current);
    });
    /* update html lang attr */
    var map={en:'en',zh:'zh-Hans',ko:'ko',yue:'zh-Hant-HK'};
    document.documentElement.lang=map[current]||'en';
  }

  function setLang(lang){
    if(!window.SKL_LANG[lang])return;
    current=lang;
    localStorage.setItem('skl_lang',lang);
    applyAll();
  }

  document.addEventListener('DOMContentLoaded',function(){
    applyAll();
    document.querySelectorAll('.lang-btn').forEach(function(b){
      b.addEventListener('click',function(){setLang(b.dataset.lang);});
    });
  });

  window.SKL_setLang=setLang;
})();
